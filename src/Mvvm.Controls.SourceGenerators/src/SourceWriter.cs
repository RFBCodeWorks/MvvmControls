        public SourceWriter Write(string format, params string[] args) { _sb.AppendFormat(format, args); _isOnNewLine = false; return this; }

        /// <summary>
        /// Write the interpolated string to the builder using AppendFormat
        /// </summary>
        /// <remarks>
        /// This is more efficent than writing an interpolated string with more than 3 arguments, but less efficient than using the <see cref="Write(string, string[])"/> overload.
        /// </remarks>
        public SourceWriter WriteFormattable(FormattableString interpolatedString)
        {
             _sb.AppendFormat(interpolatedString.Format, interpolatedString.GetArguments()); 
            _isOnNewLine = false; 
            return this; 
        }

        /// <summary>
        /// Write the interpolated string to the builder using AppendFormat
        /// </summary>
        /// <remarks>
        /// This is more efficent than writing an interpolated string with more than 3 arguments, but less efficient than using the <see cref="WriteLine(string, string[])"/> overload.
        /// </remarks>
        public SourceWriter WriteFormattableLine(FormattableString interpolatedString)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            _sb.AppendFormat(interpolatedString.Format, interpolatedString.GetArguments());
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <summary> Appends the span to the writer, then moves to the next line. </summary>
        public SourceWriter WriteLine(ReadOnlySpan<char> span)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            AppendSpan(_sb, span);
            _sb.AppendLine();
            _isOnNewLine = true; 
            return this; 
        }

        /// <inheritdoc cref="AppendFormat(StringBuilder, ReadOnlySpan{char}, ReadOnlySpan{char}, ReadOnlySpan{char}, ReadOnlySpan{char})"/>
        public SourceWriter WriteLine(ReadOnlySpan<char> formatSpan, ReadOnlySpan<char> arg1, ReadOnlySpan<char> arg2 = default, ReadOnlySpan<char> arg3 = default)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            AppendFormat(_sb, formatSpan, arg1, arg2, arg3);
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <inheritdoc cref="AppendFormat(StringBuilder, ReadOnlySpan{char}, ReadOnlySpan{char}, ReadOnlySpan{char}, ReadOnlySpan{char})"/>
        public SourceWriter WriteLine(string format, ReadOnlySpan<char> arg1, ReadOnlySpan<char> arg2 = default, ReadOnlySpan<char> arg3 = default) => WriteLine(formatSpan: format.AsSpan(), arg1, arg2, arg3);


        /// <summary> Write a single char then moves to the next line. </summary>
        public SourceWriter WriteLine(char value)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            _sb.Append(value);
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }
        
        /// <summary>
        /// Writes a line of text. Evaluates the text, ensuring even indentation across multiple lines if the text has multiple lines.
        /// </summary>
        public SourceWriter WriteLine(string text)
        {
            if (_indentation == 0)
            {
                _sb.AppendLine(text);
                _isOnNewLine = true;
                return this;
            }

            bool isFinalLine;
            ReadOnlySpan<char> remainingText = text.AsSpan();
            do
            {
                _token.ThrowIfCancellationRequested();
                ReadOnlySpan<char> nextLine = GetNextLine(ref remainingText, out isFinalLine);

                IndentIfOnNewLine();
                AppendSpan(_sb, nextLine);
                _sb.AppendLine();
                _isOnNewLine = true;
            }
            while (!isFinalLine);
            return this;
        }

        /// <inheritdoc cref="WriteLine(string, string[])"/>
        public SourceWriter WriteLine(string format, string arg1)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            _sb.AppendFormat(format, arg1);
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <inheritdoc cref="WriteLine(string, string[])"/>
        public SourceWriter WriteLine(string format, string arg1, string arg2)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            _sb.AppendFormat(format, arg1, arg2);
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <inheritdoc cref="WriteLine(string, string[])"/>
        public SourceWriter WriteLineIf(bool writeIfTrue, string format, string arg1 = "", string arg2 = "")
        {
            if (writeIfTrue) WriteLine(format, arg1, arg2);
            return this;
        }

        /// <inheritdoc cref="WriteLine(string, string[])"/>
        /// <param name="arg1"/><param name="trailingComma"/><param name="writeIfTrue"/>
        public SourceWriter WriteLineIf(bool writeIfTrue, string format, string arg1, bool trailingComma)
        {
            if (writeIfTrue)
            {
                if (_isOnNewLine && _indentation > 0) Indent();
                _sb.AppendFormat(format, arg1);
                if (trailingComma) _sb.Append(',');
                _sb.AppendLine();
                _isOnNewLine = true;
            }
            return this;
        }

        /// <summary>
        /// Write a formatted expression to the file.
        /// </summary>
        /// <remarks>The formatted expression should be on a single line.</remarks>
        /// <param name="format">A formatted expression that represents 1 line.</param>
        /// <param name="args"></param>
        /// <returns>The <see cref="SourceWriter"/></returns>
        public SourceWriter WriteLine(string format, params string[] args)
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            _sb.AppendFormat(format, args);
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <summary>
        /// Move the to next line.
        /// </summary>
        public SourceWriter WriteLine()
        {
            _sb.AppendLine();
            _isOnNewLine = true;
            return this;
        }

        /// <summary>
        /// Write a parameter using the specified <paramref name="format"/>
        /// </summary>
        /// <param name="parameterSymbol"></param>
        /// <param name="format"></param>
        /// <param name="includeSeparator">set true to append ", " after the parameter</param>
        /// <param name="asNewLine">
        /// <see langword="true"/> : {indent} {parameter} {newline}
        /// <br/> <see langword="false"/> : {parameter}
        /// </param>
        /// <param name="defaultValue">default text string " = text" to append if the parameter does not have an <see cref="IParameterSymbol.HasExplicitDefaultValue"/></param>
        /// <returns></returns>
        public SourceWriter WriteSymbol(IParameterSymbol parameterSymbol, SymbolDisplayFormat format, bool includeSeparator, bool asNewLine, string defaultValue = "")
        {
            if (asNewLine && _indentation > 0) IndentIfOnNewLine();
            _isOnNewLine = false;
            _sb.Append(parameterSymbol.ToDisplayString(format));
            if (string.IsNullOrWhiteSpace(defaultValue) == false && (!parameterSymbol.HasExplicitDefaultValue | !format.ParameterOptions.HasFlag(SymbolDisplayParameterOptions.IncludeDefaultValue)))
            {
                _sb.Append(defaultValue);
            }
            if (includeSeparator) _sb.Append(", ");
            if (asNewLine)
            {
                _sb.AppendLine();
                _isOnNewLine = true;
            }
            return this;
        }

        /// <summary>
        /// Write a method invoker
        /// </summary>
        /// <param name="methodSymbol">the method to write</param>
        /// <param name="prefix">A prefix to write before writing the method display string.</param>
        /// <param name="format">
        /// The format to write the method with.
        /// <br/> - <see cref="SymbolFormats.MethodInvokeFullyQualified"/>
        /// <br/> - <see cref="SymbolFormats.MethodInvokePrivate"/>
        /// </param>
        /// <param name="isStartOfLine">if true indents the line.</param>
        /// <param name="isEndOfLine">if true : ends the line with ";"</param>
        /// <returns></returns>
        public SourceWriter WriteSymbol(IMethodSymbol methodSymbol, SymbolDisplayFormat format, bool isStartOfLine, bool isEndOfLine, string prefix = "")
        {
            if (isStartOfLine && _indentation > 0) IndentIfOnNewLine();
            _isOnNewLine = false;
            if (!string.IsNullOrWhiteSpace(prefix)) _sb.Append(prefix);
            _sb.Append(methodSymbol.ToDisplayString(format));
            if (isEndOfLine)
            {
                _sb.Append(';').AppendLine();
                _isOnNewLine = true;
            }
            return this;
        }

        /// <summary>
        /// Writes the method signature based on the provided formats
        /// </summary>
        /// <param name="methodSymbol"></param>
        /// <param name="methodFormat">the method format to apply</param>
        /// <param name="returnTypeFormat">optional return type format. use null if not specified.</param>
        /// <param name="parameterFormat">optional parameter format to apply to each parameter. If not null, also adds the () to the writer.</param>
        /// <param name="isPartialMethod">if true, indents the line and adds 'partial' before the return type.</param>
        /// <remarks>Does NOT move to the next line.</remarks>
        public SourceWriter WriteSymbol(IMethodSymbol methodSymbol, SymbolDisplayFormat methodFormat, SymbolDisplayFormat? returnTypeFormat = null, SymbolDisplayFormat? parameterFormat = null, bool isPartialMethod = false)
        {
            var membersToExclude = parameterFormat is null ? SymbolDisplayMemberOptions.None : SymbolDisplayMemberOptions.IncludeParameters;

            // write the method signature
            if (isPartialMethod || methodSymbol.IsPartialDefinition)
            {
                if (_isOnNewLine && _indentation > 0) Indent();
                if (methodFormat.MemberOptions.HasFlag(SymbolDisplayMemberOptions.IncludeModifiers))
                {
                    WriteSymbolModifiers(methodSymbol, isPartialMethod);
                    membersToExclude |= SymbolDisplayMemberOptions.IncludeModifiers;
                }
                else
                {
                    _sb.Append("partial ");
                }
            }
            _isOnNewLine = false;

            if (returnTypeFormat is not null)
            {
                _sb.Append(methodSymbol.ReturnType.ToDisplayString(returnTypeFormat)).Append(' ');
                membersToExclude |= SymbolDisplayMemberOptions.IncludeType;
            }

            if (membersToExclude != SymbolDisplayMemberOptions.None)
            {
                methodFormat = methodFormat.WithMemberOptions(methodFormat.MemberOptions & ~membersToExclude);
            }
            _sb.Append(methodSymbol.ToDisplayString(methodFormat));
            if (parameterFormat is not null)
            {
                _sb.Append('(');
                int lastParam = methodSymbol.Parameters.Length - 1;
                for (int i = 0; i < methodSymbol.Parameters.Length; i++)
                {
                    WriteSymbol(methodSymbol.Parameters[i], parameterFormat, i < lastParam, false);
                }
                _sb.Append(')');
            }
            return this;
        }

        /// <summary>
        /// Writes the <see cref="ISymbol"/> modifiers
        /// <br/> <see langword="internal, public, private, static, abstract, virtual, override, sealed, extern, async, partial"/>
        /// </summary>
        /// <param name="isPartial">include the 'partial' keyword or not</param>
        /// <returns></returns>
        public SourceWriter WriteSymbolModifiers(ISymbol symbol, bool isPartial = false)
        {
            WriteIndent();
            switch (symbol.DeclaredAccessibility)
            {
                case Accessibility.NotApplicable: break;
                case Accessibility.ProtectedOrInternal: _sb.Append("protected internal "); break;
                case Accessibility.ProtectedAndInternal: _sb.Append("private protected "); break;
                case Accessibility.Protected: _sb.Append("protected "); break;
                case Accessibility.Internal: _sb.Append("internal "); break;
                case Accessibility.Private: _sb.Append("private "); break;
                case Accessibility.Public: _sb.Append("public "); break;
            }
            if (symbol.IsStatic) _sb.Append("static ");
            if (symbol.IsAbstract || symbol.IsVirtual || symbol.IsOverride) _sb.Append("override ");
            if (symbol.IsSealed) _sb.Append("sealed ");
            if (symbol.IsExtern) _sb.Append("extern ");
            if (symbol is IMethodSymbol ms)
            {
                if (ms.IsAsync) _sb.Append("async ");
                if (ms.IsPartialDefinition || isPartial) _sb.Append("partial ");
            }
            else if (isPartial)
            {
                _sb.Append("partial ");
            }
            _isOnNewLine = false;
            return this;
        }

        /// <summary>
        /// Add indentation to the start of the current line.
        /// </summary>
        public SourceWriter WriteIndent()
        {
            if (_isOnNewLine && _indentation > 0) Indent();
            return this;
        }

        private static ReadOnlySpan<char> GetNextLine(ref ReadOnlySpan<char> remainingText, out bool isFinalLine)
        {
            if (remainingText.IsEmpty)
            {
                isFinalLine = true;
                return default;
            }

            ReadOnlySpan<char> next;
            ReadOnlySpan<char> rest;

            int lineLength = remainingText.IndexOf('\n');
            if (lineLength == -1)
            {
                lineLength = remainingText.Length;
                isFinalLine = true;
                rest = default;
            }
            else
            {
                rest = remainingText.Slice(lineLength + 1);
                isFinalLine = false;
            }

            if ((uint)lineLength > 0 && remainingText[lineLength - 1] == '\r')
            {
                lineLength--;
            }

            next = remainingText.Slice(0, lineLength);
            remainingText = rest;
            return next;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        private static unsafe void AppendSpan(StringBuilder builder, ReadOnlySpan<char> span)
        {
            // There is no StringBuilder.Append(ReadOnlySpan<char>) overload in the NS2.0
            fixed (char* ptr = span)
            {
                builder.Append(ptr, span.Length);
            }
        }

        static ReadOnlySpan<char> ThrowOutOfRange(ReadOnlySpan<char> argIndex)
        {
            throw new ArgumentOutOfRangeException("format", $"Format index {{{new string(argIndex.ToArray())}}} is out of range.");
        }

        /// <summary>
        /// Appends a formatted string to the StringBuilder using <see langword="ReadOnlySpan{char}"/> arguments
        /// <br/>Supports only simple numbered placeholders: {0}, {1}, {2}.
        /// </summary>
        public static void AppendFormat(StringBuilder sb, ReadOnlySpan<char> format, ReadOnlySpan<char> arg1, ReadOnlySpan<char> arg2 = default, ReadOnlySpan<char> arg3 = default)
        {
            int i = 0;
            while (i < format.Length)
            {
                char c = format[i];

                if (c == '{')
                {
                    int start = ++i; // move past '{'

                    // find matching '}'
                    while (i < format.Length && format[i] != '}')
                        i++;

                    if (i >= format.Length)
                        throw new FormatException("Input string was not in a correct format: missing closing brace '}'.");

                    // parse placeholder number
                    var argIndex = format.Slice(start, i - start);
                    
                    
                    if (argIndex.Length > 1)
                    {
                        ThrowOutOfRange(argIndex);
                    }
                    ReadOnlySpan<char> arg = argIndex[0] switch
                    {
                        '0' => arg1,
                        '1' => arg2,
                        '2' => arg3,
                        _ => ThrowOutOfRange(argIndex)
                    };
                    AppendSpan(sb, arg);
                    i++; // skip '}'
                }
                else
                {
                    // append literal segment
                    int start = i;
                    while (i < format.Length && format[i] != '{')
                        i++;

                    AppendSpan(sb, format.Slice(start, i - start));
                }
            }
        }
